package day45;
public class Main {
    public static void main(String[] args) {
        //operator logika OR
        //operator logika or hanya akan menampilkan false apabila kedua nilai sebelah kiri atau kanannya semuanya false, sisanya true
        int pertama = 4;
        int kedua = 5;
        System.out.println(pertama > kedua || kedua > pertama);
    }
    
}
