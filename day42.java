public class Main {
    public static void main(String[] args) {
        //operator penugasan
        int a = 5;
        int b = 2;
        boolean samaDengan = a == b; //sama dengan
        System.out.println(samaDengan);
        boolean tidakSamaDengan = a != b; //tidak sama dengan
        System.out.println(tidakSamaDengan);
    }
}
